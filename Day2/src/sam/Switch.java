package sam;

public class Switch {

	public static void main(String[] args) {
	int x=80;
		switch(x)
		{
		case 10:
		System.out.println("case 10 is matched");
		
		case 20:
		System.out.println("case 20 is matched");
		 
		case 40:
		System.out.println("case 40 is matched");
		default :
			System.out.println("Cases are not matched");
		 
		
		}
      
	}

}
