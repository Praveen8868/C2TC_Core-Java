package sam;

public class ForEachLoop {

	public static void main(String[] args) {
		int a[] = {1,2,3,4,5};
		 for(int i : a) {
			 System.out.println(i);
			 
		 }
		 char ch[] = {'p','r','a','v','e','e','n' };
		 for(char s : ch) {
			 System.out.print(s);
		 }
		 String s1[] = {"Peri ","Institute ","of ","Technology"};
		 for(String p : s1) {
			 System.out.print(p);
		 }

	}

}
